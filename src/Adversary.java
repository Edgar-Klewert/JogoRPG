class Adversary {
    private String nome;
    private int pv;
    private int dano;
    private int defesa;
    private int agilidade;
    private boolean defesaDobrada;

    public Adversary(String nome, int pv, int dano, int defesa, int agilidade) {
        this.nome = nome;
        this.pv = pv;
        this.dano = dano;
        this.defesa = defesa;
        this.agilidade = agilidade;
    }
}
